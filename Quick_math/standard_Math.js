



// Math.min()
console.log(Math.min(2, 3, 1));
// expected output: 1

console.log(Math.min(-2, -3, -1));
// expected output: -3

var array1 = [2, 3, 1];

console.log(Math.min(...array1));
// expected output: 1


// Math.max()
console.log(Math.max(2, 3, 1));
// expected output: 3

console.log(Math.max(-2, -3, -1));
// expected output: -1

var array1 = [-2, -3, -1];

console.log(Math.max(...array1));
// expected output: -1
